const char hex_digit[17] = "0123456789ABCDEF";

int main(int argc, char *argv[])
{
    int i, j, k;
    ULONG tmp;
    ULONG rem[3];
    ULONG rom[3];
    
    FILE *fh;
    
    fh = fopen("pal_prom.mem", "w");
    
    if (fh)
    {
        for (k = 0; k < 256; k ++) // Palette index
        {
            rem[0] = 0;
            rem[1] = 0;
            rem[2] = 0;
            for (j = 0; j < 4; j ++) // Pixel index
            {
                for (i = 0; i < 3; i ++) // R,G,B
                {
                    tmp = (((ULONG)clut_1943[k][i] * (ULONG)clut_coef[j]) >> 8) + rem[i];
                    if (tmp > 255)
                    {
                        // Blooming case
                        rem[i] = tmp - 255;
                        rom[i] = 0xF;
                    }
                    else
                    {
                        // Dithering case
                        rem[i] =  tmp % 0x11;
                        rom[i] = (tmp / 0x11);
                    }
                }
                fprintf(fh, "%c%c\n0%c\n",
                        hex_digit[rom[1]],  // G
                        hex_digit[rom[0]],  // R
                        hex_digit[rom[2]]); // B
            }
        }
        fclose(fh);
    }
    
    return 0;
}
